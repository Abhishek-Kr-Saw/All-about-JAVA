public class Main {
    public static void main(String[] args) {

        // Arithmetic Operators[P-E-D-M-A-S]

        int x = 10;
        int y = 2;
        int z;

        z = x + y; // ADDITION
        z = x - y; // SUBTRACTION
        z = x * y; // MULTIPLICATION
        z = x / y; // DIVISION
        z = x % 3; // MODULUS

        System.out.println(z);

    }
}
